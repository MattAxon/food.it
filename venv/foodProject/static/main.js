function searchFood(term){
  var newUrl = 'search?term=' + term;
  $.ajax({
      type: 'GET',
      url: newUrl,
      success: function(result){
          if(result == "error bad user input"){
            searchbarLabel = document.getElementById('searchbarLabel');
            searchbarLabel.setAttribute('style', 'visibility: visible;');
          }else{
            console.log(result.meals[0]);
            displayFood(result.meals[0]);
            let card = document.getElementById('resultCard');
            card.setAttribute('style', 'visibility: visible;');
          }
      },
      error: function(error){
        console.log('error');
          console.log(error);
      }
  });
}

function displayFood(result){
  document.getElementById('foodNameDisplay').innerText = result.strMeal;
  document.getElementById("foodArea").innerText = result.strArea;
  document.getElementById('foodCategory').innerText = result.strCategory;
  let imgURL = result.strMealThumb;
  let ingridientList = [];
  let messurementList = [];
  populateFoodLists(result, messurementList, ingridientList);
  console.log(ingridientList, messurementList);
   document.getElementById('cardImg').setAttribute('src', imgURL);
   populateTable(messurementList, ingridientList);
   document.getElementById('directionText').innerText = result.strInstructions;
}

function searchCloseClicked(){
  let card = document.getElementById('resultCard');
  card.setAttribute('style', 'visibility: hidden;');
}

function populateFoodLists(result, measureList, ingridientList){
  for(let i = 1; i <= 20; i++){
    let measureStr = "strMeasure" + i;
    let ingridientStr = "strIngredient" + i;
    if(result[ingridientStr] == ""){
      break;
    }else{
      ingridientList.push(result[ingridientStr]);
      measureList.push(result[measureStr]);
    }
  }
}

function populateTable(measureList, ingridientList){
  let tableBody = document.getElementById('tableBody');
  let newTableBody = document.createElement('tbody');
  newTableBody.setAttribute('id', 'tableBody');
  for(let i = 0; i < measureList.length; i++){
    let newTR = document.createElement('tr');
    let ingridientCell = newTR.insertCell()
    let measurementCell = newTR.insertCell();
    ingridientCell.innerText = ingridientList[i];
    measurementCell.innerText = measureList[i];
    let trIDString = "row" + i;
    let ingridientIDString = "ingridient" + i;
    let measurementIDString = "measurement" + i;
    newTR.setAttribute('id', trIDString);
    ingridientCell.setAttribute('id', ingridientIDString);
    measurementCell.setAttribute('id', measurementIDString);
    newTR.setAttribute('class', 'Row');
    ingridientCell.setAttribute('class', 'ingridientCell');
    measurementCell.setAttribute('class', 'measurementCell');
    newTableBody.appendChild(newTR);
  }
  tableBody.parentElement.replaceChild(newTableBody, tableBody);
}

